# create four functions add, substract, multiply, and divide
# each function takes 2 parameters, a and b.
# return the result and test the various functions

def add(a,b):
  result = a + b
  return result

def substract(a,b):
  result = a - b
  return result

def multiply(a,b):
  result = a * b
  return result

def divide(a,b):
  result = a / b
  return result

sum = multiply(5,5)
